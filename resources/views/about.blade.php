                    <p style="text-align: justify">Suntem o echipă de studenți de la <a href="https://www.info.uaic.ro">Facultatea de Informatică</a> din Iași ce dorește a crea o aplicație destinată colectării și comparării temelor de laborator. Temele pot fi comparate atât vizual, cât și automat, pe platformă. Aplicația generează rapoarte pe teme primite, autorii temelor, teme grupate in funcție de materie și alte informații. De asemenea, aceasta permite exportarea temelor și a rapoartelor în formate editabile.</p>
                    <p style="text-align: justify">Interesul în informatică la universitatea "Alexandru Ioan Cuza" din Iaşi datează din perioada 1958-1960, când Profesorul Adolf Haimovici a iniţiat o serie de conferinţe pe "Matematici aplicate şi Informatică". În 1960, Profesorul Adolf Haimovici a ţinut primul curs de "Elemente de informatică", în timp ce în 1961 Profesorul Costică Cazacu introduce în cursul său de "Matematici Complementare", elemente de Teorie a programării şi Teoria jocurilor. În 1962, cursul de "Maşini Calculabile" a fost adăugat la curricula Facultăţii de Matematică. Prima carte românească în acest domeniu a fost Calculatoare Universale şi Teoria programării, publicată în 1968.</p>
                    <p style="text-align: justify">Catedra de maşini calculabile a fost întemeiată în 1965, ca parte a Facultăţii de matematică; cu prima promoţie în 1970. În 1971 şi-a schimbat numele în Informatică, iar în 1972 Profesorul Adolf Haimovici a devenit primul şef de catedră al acestui departament nou.</p>
                    <p style="text-align: justify">În 1990, catedra de Informatică a fost împărţită în două noi departamente: catedra de informatică şi catedra de matematici aplicate. În 1991-1992 primul a devenit Facultatea de Informatică, cu două catedre: catedra de Informatică (cu durata studiilor de 4 ani) şi Colegiul de Tehnologia Informaţiei (cu durata studiilor de 3 ani).</p>
                    <p style="text-align: justify">Din anul 1995, funcţionează cursurile de master în Sisteme Distribuite. Mai târziu, s-au adăugat două noi programe de master: Lingvistică Computaţională şi Optimizare Computaţională.</p>
                    <p style="text-align: justify">Acum, Facultatea de Informatică numără peste 1500 de studenţi şi are 50 de membri în personalul academic.</p>
